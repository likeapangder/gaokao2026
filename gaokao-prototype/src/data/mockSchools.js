/** 辅助：根据 2025 年位次生成近 3 年历史（逐年微增 3-5%）*/
function makeHistory(rank2025, scoreBase) {
  const r25 = rank2025
  const r24 = Math.round(r25 * 1.04)
  const r23 = Math.round(r24 * 1.04)
  return [
    { year: 2023, minScore: scoreBase - 4, minRank: r23 },
    { year: 2024, minScore: scoreBase - 2, minRank: r24 },
    { year: 2025, minScore: scoreBase,     minRank: r25 },
  ]
}

